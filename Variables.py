# variables en Python

# Declaración e inicialización de variables
edad =28
altura = 1.65
pais = 'México'

# acceder a las variables
print('Valores iniciales:')
print('Edad:', edad)
print('Altura:', altura)
print('Pais:', pais)

# modificar el valor de una variable
edad = 30
altura = 1.68

# Acceder a las variables
print('Valores modificados')
print('Edad:', edad)
print('Altura:', altura)
print('Pais:', pais)

# en python el tipo es dinámico
edad = 'treinta'
print('Edad:', edad)

