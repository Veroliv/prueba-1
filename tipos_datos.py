# ejemplo de tipos de datos en Python

# Entero
edad = 30
print('Edad:', edad)

# numero con punto flotante
altura = 1.75
print('Altura:', altura)

# cadena de texto
nombre = 'Ana'
print('Nombre:', nombre)

# tipo boolean
es_estudiante = False
print('Estudiante:', es_estudiante)

# none, ausencia de valor
direccion = None
print('Direccion:', direccion)